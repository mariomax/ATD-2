    <div class="heading">
        <div class="fai"><i class="fas fa-mobile-alt"></i></div>
        <div class="title"><h3>Mobile First</h3></div>
    </div>
    <p>The “mobile first” concept implies a website was designed for ease of use and fast load times on smart phones – where most web traffic originates. When the site is viewed on larger screens, additional styling and functionality contribute to the user’s experience (progressive enhancement). Google downgrades search rankings (SEO) when a site isn’t mobile optimized!</p>

    <section class="heading">
        <div class="fai"><i class="fab fa-wordpress-simple"></i></div>
        <div class="title"><h3>WordPress</h3></div>
    </section>
    <p>use the right platform for your site. A mobile first site will work if you only need a few images, an about section, and social media links. WordPress should be used for more robust sites with galleries, forms, a shop, etc. WordPress is FREE, and is the most popular and user-friendly content management system (CMS) on the web. Its dashboard makes it a snap to update and/or edit content.</p>

    <section class="heading">
        <div class="fai"><i class="far fa-keyboard"></i></div>
        <div class="title"><h3>Usability</h3></div>
    </section> 
    <p>One of the most important – and often overlooked – concepts in web design is thinking about the site’s visitors. Is the focus of the site clear? And is the site user-friendly? There are actually measurable usability factors too. Hopefully, providing your visitors with a quality experience (coupled with style and functionality) will bring them back again.</p>
    
