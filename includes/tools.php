			</section>
			<p>Use the right platform for your site. A mobile first site will work if you only need a few images, an about section, and social media links. WordPress is used for more robust sites with galleries, forms, a shop, etc. WordPress is FREE, and is the most popular and user-friendly content management system (CMS) on the web. Its dashboard makes it a snap to create, update and/or edit content.</p>
